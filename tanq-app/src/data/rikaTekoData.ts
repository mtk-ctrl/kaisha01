// 中学受験 理科〈てこのつり合い〉— 計算分野の単元データ（構造転換のひな型）
// 方式は算数/juku を踏襲: まなぶ（図解導入スライド）→ とく（演習・2段階ヒント）。
// テキストは Furigana コンポーネントの `{漢字|かんじ}` 記法。
// 全14問のモーメント計算は Ken が検算済み（各問題のコメントに検算式を残す）。

export interface TekoLoad {
  dist: number          // 支点からの距離
  w: number | null      // おもさ（null = もとめる値。図では「?」表示）
  label?: string        // 図中の補足ラベル（例: 石・くぎ）
}

export interface TekoDiagramSpec {
  mode: 'balance' | 'lever'   // balance=支点が中央の実験てこ / lever=支点が端の棒
  armUnit: string             // 'cm' | '目もり'
  wUnit: string               // 'g' | 'kg' | 'こ'
  left: TekoLoad[]            // 支点から左側（lever では支点に近い側＝作用点側）
  right: TekoLoad[]           // 支点から右側（lever では力点側）
  barWeight?: { dist: number; w: number; side: 'left' | 'right' }  // 棒の重さ（重心の位置）
  momentLeft?: string         // 答え合わせで見せるモーメント式（左）
  momentRight?: string        // 答え合わせで見せるモーメント式（右）
}

export type TekoDifficulty = 1 | 2 | 3

export interface TekoProblem {
  id: string
  title: string
  difficulty: TekoDifficulty
  problemText: string          // ふりがな記法
  answer: string               // 数値文字列
  answerUnit: string
  choices: [string, string, string, string]  // 正解+誤答3（出題時に Fisher-Yates でシャッフル）
  scaffoldHint: string         // 1回目不正解 → 考える足場（答えは言わない）
  explanation: string          // 正解後 / 2回目不正解の答え開示で見せる説明
  diagram: TekoDiagramSpec
}

export interface TekoSlide {
  kind: 'three-points' | 'moment' | 'balance' | 'tools' | 'steps'
  title: string
  points: string[]             // ふりがな記法
  diagram?: TekoDiagramSpec
}

