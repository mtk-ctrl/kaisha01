export type KouminQuestion = KouminChoiceQuestion | KouminOrderQuestion

export interface KouminChoiceQuestion {
  id: string
  level: number
  type: 'choice'
  q: string
  choices: [string, string, string, string]
  answer: 0 | 1 | 2 | 3
  hint: string
  explain: string
  fact: string
}

// ならべかえ: items は orderLabel の向き（例: はじめ → おわり）の正しい順で記述し、表示時にシャッフルする
export interface KouminOrderQuestion {
  id: string
  level: number
  type: 'order'
  q: string
  orderLabel: string
  items: string[]   // 3〜4個
  hint: string
  explain: string
  fact: string
}

