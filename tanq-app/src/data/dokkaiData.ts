// 国語〈読解〉ためしてみる版 — AI生成オリジナル文（市販文章の転載は著作権リスクのため禁止）
// 設計原則:
// - 文章はすべて書き下ろし。小3〜小4語彙。ふりがなは {漢字|かな} 記法（Furigana コンポーネント）
// - 正解は本文から一意に決まること（evidence = 根拠の文 index）。Ken が全問突き合わせ検証済み
// - 誤答は「本文を読まないと選びそうな誤読」。複数解釈できる選択肢の同居は禁止
// - hint は答えを見せない「本文にもどる足場」（1回目不正解時に表示）

export type DokkaiQType = '指示語' | '理由' | '気持ち' | '接続語' | 'だれが何をした' | '要点'

export interface DokkaiQuestion {
  type: DokkaiQType
  q: string
  choices: string[]      // 4択。index 0 が正解（出題時にシャッフル）
  answer: number
  hint: string           // 1回目不正解時の足場（答えを見せず本文へもどす）
  explain: string        // 2回目（答え合わせ）の解説。根拠文ハイライトとセットで表示
  evidence: number[]     // 根拠となる文の index（sentences 内）
}

export interface DokkaiPassage {
  id: string
  kind: '物語' | '説明'
  sentences: string[]    // 文単位の配列。解説時に evidence の文をハイライトする
  questions: DokkaiQuestion[]
}

