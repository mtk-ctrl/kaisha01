// - order 問題は orderLabel（例: 北 → 南）で並べる向きを示す

export type ChiriQuestion = ChiriChoiceQuestion | ChiriOrderQuestion

export interface ChiriChoiceQuestion {
  id: string
  level: number
  type: 'choice'
  q: string
  choices: [string, string, string, string]
  answer: 0 | 1 | 2 | 3
  hint: string
  explain: string
  fact: string
}

