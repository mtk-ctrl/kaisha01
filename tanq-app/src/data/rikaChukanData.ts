// 中学受験 理科〈水溶液と中和〉— 計算・思考分野の単元データ
// rika-circuit のひな型を踏襲。まなぶ（図解導入スライド）→ とく（演習・2段階ヒント）。
// テキストは Furigana コンポーネントの `{漢字|かんじ}` 記法。
//
// 【Ken 検証ルール（水溶液・中和の中受標準）】
//  ・指示薬の色（厳密に）:
//    リトマス紙 … 酸性: 青リトマス→赤 / アルカリ性: 赤リトマス→青 / 中性: どちらも変化なし
//    BTB液 … 酸性=黄 / 中性=緑 / アルカリ性=青
//    フェノールフタレイン … 酸性・中性=無色 / アルカリ性=赤(桃色)
//  ・中和 ＝ 酸とアルカリが打ち消し合い「水」と「塩(えん)」ができる。
//    塩酸＋水酸化ナトリウム水溶液 → 食塩(塩化ナトリウム)＋水。
//  ・「ちょうど中和」する酸とアルカリの体積比は一定（比例）。
//    例: 塩酸20cm³とアルカリ30cm³でちょうど中和 → 比 2:3。
//    塩酸40cm³なら アルカリ 60cm³でちょうど（2倍）。
//  ・中和後の液性は「ちょうど中和」を基準に、どちらが余るかで決まる:
//    酸が余る→酸性 / アルカリが余る→アルカリ性 / ちょうど→中性。
//  ・できる塩(食塩)の重さは「実際に中和した量」に比例する。
//    余った酸/アルカリは塩を作らない（過剰分は塩の量に寄与しない）。
//  全15問を上記ルールで1問ずつ検証（各問題のコメントに検算を残す）。

// ビーカーのスペック。liquid = この液の性質。
//   解答前は figure に色を出さず中立（グレー）に描く（答えバレ防止）。
//   reveal 時にのみ液性に応じた色を表示する。
// volumeBar = ちょうど中和の点を可視化する体積バー（任意）。
export type Liquid = 'acid' | 'neutral' | 'alkali'

export interface BeakerSpec {
  // 液性。null = 解答前に示さない（答えバレ防止・reveal でも null なら無色のまま）
  liquid: Liquid | null
  label?: string          // ビーカーの見出し（例: 「ビーカーA」）
  caption?: string        // 図の下に出す中立な説明（液性・色は書かない）
  // 体積バー（任意）: 酸とアルカリの体積を横棒で並べて「ちょうど中和の点」を見せる
  acidVol?: number        // 酸の体積（cm³）
  alkaliVol?: number      // アルカリの体積（cm³）
  justVol?: number        // ちょうど中和するアルカリの体積（cm³）。バーに目印線を引く
}

export type ChukanDifficulty = 1 | 2 | 3

export interface ChukanProblem {
  id: string
  title: string
  difficulty: ChukanDifficulty
  problemText: string          // ふりがな記法
  answer: string               // 選択肢の正解（文字列）
  answerUnit: string           // 単位（液性判定は '' 、体積は 'cm³' など）
  choices: string[]            // 正解+誤答（出題時に Fisher-Yates でシャッフル）
  scaffoldHint: string         // 1回目不正解 → 考える足場（答えは言わない）
  explanation: string          // 正解後 / 2回目不正解の答え開示で見せる説明
  // 図は0〜3個。比較問題は並べる。reveal でのみ液性の色を出す。
  diagrams: BeakerSpec[]
}

export interface ChukanSlide {
  kind: 'indicator' | 'neutral' | 'ratio' | 'after' | 'graph'
  title: string
  points: string[]             // ふりがな記法
  diagrams?: BeakerSpec[]
}

