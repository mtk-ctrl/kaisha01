// 中学受験 理科〈ふりこ〉— 計算・思考分野の単元データ
// rika-buoy のひな型を踏襲。まなぶ（図解導入スライド）→ とく（演習・2段階ヒント）。
// テキストは Furigana コンポーネントの `{漢字|かんじ}` 記法。
//
// 【Ken 検証ルール（ふりこの中受標準）】
//  ・ふりこの「周期（1往復にかかる時間）」は「糸の長さ」だけで決まる。
//      → おもりの重さ・ふれはば（ふれる角度）は周期に関係しない。これが中受の核心。
//  ・糸を長くすると周期は長くなる。
//  ・長さと周期は「平方の関係」… 長さ4倍 → 周期2倍、長さ9倍 → 周期3倍、長さ16倍 → 周期4倍。
//      （長さの比 a:b が平方数なら、周期の比は √a:√b。中受では表で与えられることが多い。）
//  ・1往復の時間＝周期。10往復の時間 ÷ 10 で1往復を正確に求める（はかり方）。
//  ・等時性（ガリレオ）… ふれはばが小さいうちは、ふれはばが変わっても周期は変わらない。
//  ・くぎを打ったふりこ … 途中でくぎに糸がかかると、そこから先は糸が短くなる。
//      左半分と右半分で長さがちがうので、周期は「左の半周期＋右の半周期」で合成する。
//      半周期＝その長さの周期の半分（1往復＝行き＋帰り、片道＝半周期）。
//  全14問を上記ルールで1問ずつ検証（各問題のコメントに検算を残す）。

// 図スペック。支点・糸・おもり・ふれはば角を描く。
//   求める量（周期・長さ・往復回数）は解答前に図へ実数を出さず「?」表示（答えバレ防止）。
export interface FurikoFigure {
  label?: string             // 図の見出し
  caption?: string           // 図の下の中立な説明
  lengthRatio?: number       // 糸の長さ（描画用の相対値 0.4〜1.0）。長短の見せ分けに使う
  angleDeg?: number          // ふれはばの角度（描画用・度）。既定 24
  // 表示する数値（解答前に出してよい既知量）。null は求める量（解答前は「?」）、undefined は出さない
  lengthVal?: number | null    // 糸の長さ(cm)
  lengthUnit?: string          // 長さの単位（既定 'cm'）
  periodVal?: number | null    // 周期(秒)
  periodUnit?: string          // 周期の単位（既定 '秒'）
  nail?: boolean               // くぎを打ったふりこ（支点の下にくぎを描く）
  nailRatio?: number           // くぎの位置（支点からの距離 / 全長。0〜1）
}

export type FurikoDifficulty = 1 | 2 | 3

export interface FurikoProblem {
  id: string
  title: string
  difficulty: FurikoDifficulty
  problemText: string          // ふりがな記法
  answer: string               // 選択肢の正解（文字列・単位抜き）
  answerUnit: string           // 単位（'秒' / 'cm' / '回' / '倍' など。判定系は ''）
  choices: string[]            // 正解+誤答（出題時に Fisher-Yates でシャッフル）
  scaffoldHint: string         // 1回目不正解 → 考える足場（答えは言わない）
  explanation: string          // 正解後 / 2回目不正解の答え開示で見せる説明
  diagrams: FurikoFigure[]     // 図は0〜2個。reveal でのみ求める量を出す。
}

export interface FurikoSlide {
  kind: 'length-only' | 'longer' | 'square' | 'measure' | 'isochronism'
  title: string
  points: string[]             // ふりがな記法
  diagrams?: FurikoFigure[]
}

