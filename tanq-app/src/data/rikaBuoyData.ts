// rika-chukan のひな型を踏襲。まなぶ（図解導入スライド）→ とく（演習・2段階ヒント）。
// テキストは Furigana コンポーネントの `{漢字|かんじ}` 記法。
//
// 【Ken 検証ルール（浮力の中受標準）】
//  ・浮力(g) ＝ その物が押しのけた水の重さ ＝ 水中にある体積(cm³) × 1(g/cm³)。
//    （水は 1cm³ = 1g。だから「水中の体積(cm³)」がそのまま「浮力(g)」になる。）
//  ・完全に沈めた物 … 浮力 = 物の全体積(cm³) × 1。
//  ・ばねばかりの読み(g) ＝ 物の重さ(g) − 浮力(g)。
//  ・水に浮く物 … 重さ = 浮力（つり合う）。
//      → 押しのけた水の体積(cm³) = 物の重さ(g)。水面より下に沈む体積がこの値。
//  ・沈む / 浮く の判定 … 重さ > 全体積分の浮力なら沈む、重さ ≦ なら浮く。
//      （1cm³あたりの重さ＝密度が 1g/cm³ より大きければ沈む、小さければ浮く。）
//  ・水面の上昇 … 入れた物の「水中にある体積」のぶんだけ水位が上がる
//      （= 押しのけた水の体積 ÷ 容器の底面積）。
//  ・台はかり（容器ごと台ばかりに乗せる）… 物を沈める/浮かべると、
//      台はかりの読みは「物の重さ」のぶんだけ増える（系全体の重さで考える）。
//  全14問を上記ルールで1問ずつ検証（各問題のコメントに検算を残す）。

// 物体スペック。water面・物体・ばねばかりの描画に使う。
//   求める量（浮力・ばねばかりの読み・水面上昇など）は解答前に図へ実数を出さず「?」表示（答えバレ防止）。
export type FloatState = 'sink' | 'float' | 'submerged'
// sink      = 底に沈んでいる（完全水没・底につく）
// submerged = 完全に水中だがばねばかりでつるしている
// float     = 水面に浮いている（一部が水面上）

export interface BuoyFigure {
  state: FloatState
  label?: string          // 図の見出し（例: 「鉄のおもり」）
  caption?: string        // 図の下の中立な説明
  hasSpring?: boolean     // ばねばかりでつるしているか
  // 表示する数値（解答前から出してよい既知量）。null は出さない。
  springRead?: number | null   // ばねばかりの読み(g)。求める量なら null（「?」表示）
  weight?: number | null       // 物の重さ(g)
  buoyancy?: number | null     // 浮力(g)。求める量なら null（「?」表示）
  // 浮く物体: 全体に対する「水面より上に出ている割合」(0〜1)。描画用。null なら半分で描く
  aboveRatio?: number | null
}

export type BuoyDifficulty = 1 | 2 | 3

export interface BuoyProblem {
  id: string
  title: string
  difficulty: BuoyDifficulty
  problemText: string          // ふりがな記法
  answer: string               // 選択肢の正解（文字列・単位抜き）
  answerUnit: string           // 単位（'g' / 'cm³' / 'cm' など。判定系は ''）
  choices: string[]            // 正解+誤答（出題時に Fisher-Yates でシャッフル）
  scaffoldHint: string         // 1回目不正解 → 考える足場（答えは言わない）
  explanation: string          // 正解後 / 2回目不正解の答え開示で見せる説明
  diagrams: BuoyFigure[]       // 図は0〜2個。reveal でのみ求める量を出す。
}

