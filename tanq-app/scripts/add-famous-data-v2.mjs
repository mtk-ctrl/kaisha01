let content = readFileSync(DATA_PATH, 'utf-8');

const arrMatch = content.match(/export const PREFECTURES: Prefecture\[\] = (\[[\s\S]*?\])\n\n/);
if (!arrMatch) throw new Error('PREFECTURES array not found');

const PREFECTURES = JSON.parse(arrMatch[1]);

let addedCount = 0;
let skippedCount = 0;

for (const pref of PREFECTURES) {
  const additions = ADDITIONS[pref.id];
  if (!additions) continue;

  const existingNames = new Set(pref.famous.map(f => f.name));

  for (const item of additions) {
    if (existingNames.has(item.name)) {
      skippedCount++;
      continue;
    }
    pref.famous.push(item);
    existingNames.add(item.name);
    addedCount++;
  }
}

const newArr = JSON.stringify(PREFECTURES, null, 2);
const newContent = content.replace(
  /export const PREFECTURES: Prefecture\[\] = \[[\s\S]*?\]\n\n/,
  `export const PREFECTURES: Prefecture[] = ${newArr}\n\n`
);

writeFileSync(DATA_PATH, newContent, 'utf-8');
